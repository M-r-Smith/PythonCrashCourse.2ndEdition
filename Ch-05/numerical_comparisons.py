# Numerical Comparisons
# The following code checks whether a person is 18 years old:
age = 18
age == 18

# You can also check if two numbers are not equal. For example, the following code prints a message if the given answer is not correct:
answer = 17
if answer != 42:
    print("That is not the correct answer. Please try again!")

# You can include various mathematical comparisons in your conditional statements as well, such as less than, less than or equal to, greater than , and greater than or equal to:
age = 19
age < 21
age <= 21