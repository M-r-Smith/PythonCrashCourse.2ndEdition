# An if-else statement is like an if statement but the else statement allows you to define 
# an action or set of actions that are executed when the conditional test fails.
# Lets write a similar message about voting but this time if someone isn't old enough the message will portray the opposite.
age = 17
if age >= 18:
    print("You are old enough to vote!")
    print("Have you registered to vote yet?")
else:
    print("Sorry, you are too young to vote.")
    print("Please register to vote as soon as you turn 18!")
# This code works because you are either old enough to vote or not.


    