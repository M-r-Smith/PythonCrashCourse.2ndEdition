# The first item in python always starts with 0.
# The following asks for the bicycles at index 1 and index 3:

# This code returns the second and fourth bicycles in the list:
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[1])
print(bicycles[3])

# You can also access the last elements on a list by asking for item at index -1
# This code returns the value 'specialized'
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[-1])

# In Python, square brackets ([]) indicate a list, and individual elements in the list are separated by commas.
