# You can access elements by telling python the position, or index, of the item desired.
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[0])

# You can also make the format neater by using the title() method:
bicycles = ['trek', 'cannondale', 'redline', 'specialized',]
print(bicycles[0].title())

