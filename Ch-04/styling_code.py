# When someone wants to make a change to the python language, they write a 'Python Enhanced Proposal' (PEP). 
# One of the oldest PEP's is PEP 8, which instructs python programmers on how to style their code. 
# Given the choice between whether the code is easier to read or write, its best if its easier to read



# Indentation
# PEP 8 recommends that you use 4 spaces per indentation level. Using four spaces improves readability while leaving room for multiple levels of indentation on each line.



